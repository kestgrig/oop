export const matematika = {
    add: (a, b) => a + b,
    diff: (a, b) => a - b,
    multi: (a, b) => a * b,
    div: (a, b) => a / b,
    mod: (a, b) => a % b,
};

console.log(matematika.add(7, 5));
console.log(matematika.diff(7, 5));
console.log(matematika.multi(7, 5));
console.log(matematika.div(7, 5));
console.log(matematika.mod(7, 5));